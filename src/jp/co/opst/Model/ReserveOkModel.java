package jp.co.opst.Model;

public class ReserveOkModel {

	private String depDay;
	private String arrDay;
	private String carNum;
	private int price;

	public String getDepDay() {
		return depDay;
	}

	public void setDepDay(String depDay) {
		this.depDay = depDay;
	}

	public String getArrDay() {
		return arrDay;
	}

	public void setArrDay(String arrDay) {
		this.arrDay = arrDay;
	}

	public String getCarNum() {
		return carNum;
	}

	public void setCarNum(String carNum) {
		this.carNum = carNum;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
